def calculate_atomic_sum(elements):
    total_sum = 0
    atomic_symbols = []
    for element in elements:
        if element in atomic_numbers:
            atomic_sum = atomic_numbers[element]
            total_sum += atomic_sum
            atomic_symbols.append(f"{element} ({atomic_sum})")
        else:
            print(f"Invalid element symbol: {element}")
    return total_sum, atomic_symbols

def main():
    input_elements = input("Enter the atomic symbols separated by spaces: ").split()
    total_sum, atomic_symbols = calculate_atomic_sum(input_elements)
    
    if atomic_symbols:
        equation = " + ".join(atomic_symbols)
        print(f"{equation} = {total_sum}")
    else:
        print("No valid atomic symbols were entered.")

if __name__ == "__main__":
    main()
